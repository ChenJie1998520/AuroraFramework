        /// 左边两个角为圆角
        /// </summary>
        Left = 2,

        /// <summary>
        /// 左上角为圆角
        /// </summary>
        LeftTop = 3,

        /// <summary>
        /// 左下角为圆角
        /// </summary>
        LeftBottom = 4,

        /// <summary>
        /// 右边两个角为圆角
        /// </summary>
        Right = 5,

        /// <summary>
        /// 右上角为圆角
        /// </summary>
        RightTop = 6,

        /// <summary>
        /// 右下角为圆角
        /// </summary>
        RightBottom = 7,

        /// <summary>
        /// 上边两个角为圆角
        /// </summary>
        Top = 8,

        /// <summary>
        /// 下边两个角为圆角
        /// </summary>
        Bottom = 9,
    }
}
